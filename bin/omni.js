#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const prompts = require('prompts');
const chalk = require('chalk');
const { program } = require('commander');

// Hàm hỗ trợ: Tìm file cấu hình hiện tại trong thư mục
function findConfigFile() {
    const files = ['.cursorrules', '.windsurfrules', 'CLAUDE.md', 'SYSTEM_PROMPT.md'];
    for (const file of files) {
        if (fs.existsSync(path.join(process.cwd(), file))) return file;
    }
    return null;
}

program
    .name('omni')
    .description('Trình quản lý hệ tư tưởng Omni-Coder Kit')
    .version('1.0.0');

// LỆNH 1: omni init
program
    .command('init')
    .description('Khởi tạo DNA và workflow cho dự án mới')
    .action(async () => {
        console.log(chalk.cyan.bold('\n🚀 Khởi tạo Omni-Coder Kit!\n'));

        const response = await prompts([
            {
                type: 'select',
                name: 'ide',
                message: 'Bạn đang sử dụng AI IDE/Công cụ nào?',
                choices: [
                    { title: 'Cursor', value: 'cursor' },
                    { title: 'Windsurf', value: 'windsurf' },
                    { title: 'Claude Code (CLI)', value: 'claudecode' },
                    { title: 'Generic (SYSTEM_PROMPT.md)', value: 'generic' }
                ],
            },
            {
                type: 'select',
                name: 'strictness',
                message: 'Mức độ kỷ luật?',
                choices: [
                    { title: 'Hardcore (Ép 100% SDLC)', value: 'hardcore' },
                    { title: 'Flexible (Cho phép bypass lỗi vặt)', value: 'flexible' }
                ]
            },
            {
                type: 'multiselect',
                name: 'stacks',
                message: 'Chọn Tech Stack (Space để chọn):',
                choices: [
                    { title: 'React / Next.js', value: 'react-next' },
                    { title: 'Hono + PostgreSQL', value: 'hono-pg' },
                    { title: 'Automation (Bot/Sheets)', value: 'automation-bot' }
                ],
                instructions: false
            }
        ]);

        if (!response.ide) {
            console.log(chalk.red('Hủy bỏ.'));
            return;
        }

        const templatesDir = path.join(__dirname, '..', 'templates');
        const mindset = fs.readFileSync(path.join(templatesDir, 'core', 'karpathy-mindset.md'), 'utf-8');
        const hygiene = fs.readFileSync(path.join(templatesDir, 'core', 'claudex-hygiene.md'), 'utf-8');
        const sdlc = fs.readFileSync(path.join(templatesDir, 'workflows', 'superpower-sdlc.md'), 'utf-8');
        const pmTemplates = fs.readFileSync(path.join(templatesDir, 'workflows', 'pm-templates.md'), 'utf-8');
        const skillManager = fs.readFileSync(path.join(templatesDir, 'workflows', 'skill-manager.md'), 'utf-8');

        let finalRules = `> Generated by Omni-Coder Kit\n\n${mindset}\n\n${hygiene}\n\n${sdlc}\n\n${skillManager}\n\n${pmTemplates}\n\n`;

        if (response.stacks && response.stacks.length > 0) {
            finalRules += `## TECH STACK SPECIFIC RULES\n\n`;
            for (const stack of response.stacks) {
                finalRules += fs.readFileSync(path.join(templatesDir, 'stacks', `${stack}.md`), 'utf-8') + '\n\n';
            }
        }

        let fileName = response.ide === 'cursor' ? '.cursorrules' :
            response.ide === 'windsurf' ? '.windsurfrules' :
                response.ide === 'claudecode' ? 'CLAUDE.md' : 'SYSTEM_PROMPT.md';

        if (response.strictness === 'flexible') {
            finalRules += `\n## FAST-TRACK MODE\n- **[/om:hotfix]:** Use to bypass PM/Architect planning for minor fixes. Add note in \`tech-debt.md\`.\n`;
        }

        fs.writeFileSync(path.join(process.cwd(), fileName), finalRules, 'utf-8');
        console.log(chalk.green.bold(`\n✅ Thành công! Đã tạo file: ${fileName}\n`));
    });

// LỆNH 2: omni add <skill>
program
    .command('add <skill>')
    .description('Bơm thêm một kỹ năng chuyên sâu vào file cấu hình hiện tại')
    .action((skill) => {
        const configFile = findConfigFile();
        if (!configFile) {
            console.log(chalk.red.bold('\n❌ Không tìm thấy file Omni nào ở thư mục này. Hãy chạy "omni init" trước.\n'));
            return;
        }

        const skillPath = path.join(__dirname, '..', 'templates', 'stacks', `${skill}.md`);
        if (!fs.existsSync(skillPath)) {
            console.log(chalk.red.bold(`\n❌ Kỹ năng "${skill}" không tồn tại trong hệ thống.\n`));
            return;
        }

        const skillContent = fs.readFileSync(skillPath, 'utf-8');
        fs.appendFileSync(path.join(process.cwd(), configFile), `\n\n${skillContent}\n`, 'utf-8');

        console.log(chalk.green.bold(`\n✅ Đã bơm kỹ năng [${skill}] vào file ${configFile} thành công!\n`));
    });

// LỆNH 3: omni list
program
    .command('list')
    .description('Xem danh sách các kỹ năng (skills) đang có sẵn trong kho')
    .action(() => {
        const stacksDir = path.join(__dirname, '..', 'templates', 'stacks');
        if (!fs.existsSync(stacksDir)) {
            console.log(chalk.red('\n❌ Không tìm thấy thư mục templates/stacks.\n'));
            return;
        }

        const files = fs.readdirSync(stacksDir).filter(f => f.endsWith('.md'));
        console.log(chalk.cyan.bold('\n📦 Danh sách các kỹ năng hiện có:\n'));

        if (files.length === 0) {
            console.log(chalk.yellow('  (Chưa có kỹ năng nào được tạo)'));
        } else {
            files.forEach(file => {
                const skillName = file.replace('.md', '');
                console.log(chalk.green(`  - ${skillName}`));
            });
        }
        console.log(chalk.white(`\n💡 Sử dụng lệnh: `) + chalk.yellow(`omni add <tên-kỹ-năng>`) + chalk.white(` để nhúng vào dự án.\n`));
    });



program.parse(process.argv);